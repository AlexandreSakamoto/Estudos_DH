console.log(nome);

// string usando aspas simples
let sobreNome = 'Sakamoto';
console.log(sobreNome);

//inteiro
let idade=41;
console.log(idade);

//decimal
let altura=1.81;
console.log(altura);

// booleano true
let diaEstaChuvoso = true;
console.log(diaEstaChuvoso);

// booleano false
let vouParaPraia = false;
console.log(vouParaPraia);

// valor not a number ou NaN  
// A informação não é um numero, sting ou booleano. 
// o java Não conseguiu calcular ex.: 2 * a  
let naoNumerico = NaN;
console.log(naoNumerico);

//  Valores Null - Valor desconhecido (não sabemos ainda quantidade ainda ex.:)
let jogadores = null;
console.log(jogadores);

//  Undefined - ainda não tem um valor definido 
// (armazena um espaço na memória, mas não temos o dados)
let espectadores=undefined;
espectadores = 10;
console.log(espectadores);

//  Objeto Literal
